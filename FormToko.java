     public final void tampil_database(){
        Object [] baris = {"Id","Nama","Harga","Jumlah"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_tokokomputer.setModel(tabmode);
        try {
            hasil = aa.tampilData();
            while (hasil.next()){
                aa.setID(hasil.getString("id"));
                aa.setNama(hasil.getString("nama"));
                aa.setHarga(hasil.getString("harga"));
                aa.setJumlah(hasil.getString("jumlah"));
                String[] data = {aa.getID(), aa.getNama(), aa.getHarga(), aa.getJumlah()};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
        }
    }
     
